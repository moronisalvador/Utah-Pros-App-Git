// WHAT THIS DOES (plain language):
//   The brains behind the upr_code_context MCP tool. Given a plain-English
//   feature name like "invoice payment reconciliation", it looks up a checked-in
//   keyword map (codeIndex.js) and returns a compact list of where that feature
//   lives in the UPR codebase — the relevant pages, components, background
//   workers, database functions, tables, tests, coding-standard docs, and any
//   "gold-standard" implementation the rules docs point to. It expands UPR's
//   business vocabulary (claim/job/estimate/invoice/QBO/Encircle/Twilio…) with
//   synonyms so a loose phrase still finds the right files. Pure functions only —
//   no network, no database, no filesystem — so it runs anywhere and is testable.
//
// WHERE IT LIVES:
//   Imported by:  src/tools.js  (the upr_code_context tool's run())
//   Reads:        src/codeIndex.js  (generated by `npm run build-index`)
//
// DEPENDS ON:
//   Internal:  ./codeIndex.js (the generated INDEX)
//
// NOTES / GOTCHAS:
//   - Read-only by construction: it only reads the imported constant. No writes.
//   - The output is deliberately capped (paths only, small per-category limits)
//     to stay well under ~2k tokens per call.
//   - Synonym clusters are the one place to teach the tool new UPR vocabulary;
//     add a term to a cluster and both directions expand automatically.
// ════════════════════════════════════════════════

